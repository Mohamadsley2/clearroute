# ── Detail View ───────────────────────────────────────────────────────────────
def show_detail(detection):
    """Renders the full detail panel for a single detection."""
    col_info, col_img = st.columns([1, 1], gap="large")

    with col_info:
        # Read from cache — instant if already loaded, 'Loading...' if not yet
        address = st.session_state.addr_cache.get(
            (detection["Lat"], detection["Lon"]), "Loading address..."
        )
        st.markdown("**Detection Info**")
        st.markdown(f"- **Address:** {address}")
        st.markdown(f"- **Type:** {detection['Type']}")
        st.markdown(f"- **Coordinates:** {detection['Lat']}, {detection['Lon']}")
        st.markdown(f"- **Confidence:** {detection['Confidence']:.0%}")
        st.markdown(f"- **Score:** {detection['Score']}")
        st.markdown(f"- **Timestamp:** {detection['Time']}")
        st.markdown(f"- **Rank:** #{detection['Rank']} in the priority queue")

    with col_img:
        st.markdown("**Annotated Image**")
        frame_path = detection.get("FramePath")
        if frame_path and Path(frame_path).exists():
            # Real data from detect.py — show the YOLO-annotated frame
            st.image(
                frame_path,
                caption=f"Erkannter Müll — {detection['Time']}",
                use_container_width=True,
            )
        else:
            # Demo data (dados_exemplo.json) has no images — keep the placeholder
            st.info(
                "📷 Image will be available after YOLO integration.\n\n"
                "*(Bild wird nach YOLO-Integration verfügbar)*",
                icon="🔧",
            )

st.subheader("🔍 Detail View")
st.caption("Select a detection from the priority queue to inspect it")

options      = [f"#{r['Rank']} — {r['Type']} @ {r['Time']}" for r in rows]
chosen_label = st.selectbox("Detection", options, index=0)
show_detail(rows[options.index(chosen_label)])

# ── Progressive address loading ───────────────────────────────────────────────
# This block runs AFTER the entire dashboard has been sent to the browser.
# It fills addr_progress_slot (the reserved slot above the map) with a live
# progress bar, fetches each missing address, then triggers one rerun so the
# map and detail panel refresh with real addresses in their popups.

unique_coords = list({(d["lat"], d["lon"]) for d in data})
unloaded      = [(lat, lon) for lat, lon in unique_coords
                 if (lat, lon) not in st.session_state.addr_cache]

if unloaded:
    n_total = len(unique_coords)

    for lat, lon in unloaded:
        n_done = len(st.session_state.addr_cache)
        # Update the slot reserved above the map
        addr_progress_slot.progress(
            n_done / n_total,
            text=f"Loading addresses... ({n_done}/{n_total})",
        )
        # get_address is @st.cache_data — sleep only runs on first-ever fetch
        st.session_state.addr_cache[(lat, lon)] = get_address(lat, lon)

    # All addresses are now in session_state — clear bar and refresh the map
    addr_progress_slot.empty()
    st.rerun()
