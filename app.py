# ── Progressive address loading ───────────────────────────────────────────────
# This block runs AFTER the entire dashboard has been sent to the browser.
# It fills addr_progress_slot (the reserved slot above the map) with a live
# progress bar, fetches each missing address, then triggers one rerun so the
# map and detail panel refresh with real addresses in their popups.

