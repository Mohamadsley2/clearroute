# ── Priority Queue ────────────────────────────────────────────────────────────
def build_priority_table(detections):
    """Returns detections sorted by score (highest first), with rank added."""
    scored = []
    for d in detections:
        scored.append({
            "Type":       d["typ"],
            "Lat":        d["lat"],
            "Lon":        d["lon"],
            "Confidence": d["konfidenz"],
            "Time":       d["zeitstempel"],
            "Score":      calculate_score(d),
            # frame_path is only present in real data — None for dados_exemplo.json
            "FramePath":  d.get("frame_path"),
        })
    scored.sort(key=lambda r: r["Score"], reverse=True)
    for i, row in enumerate(scored):
        row["Rank"] = i + 1
    return scored

