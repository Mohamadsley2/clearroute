# ── Scoring ───────────────────────────────────────────────────────────────────
TYPE_WEIGHTS = {
    "Müll":       1.0,
    "Verpackung": 1.0,
    "Flasche":    1.2,
    "Becher":     0.8,
}

def calculate_score(detection):
    """Score = confidence × type weight. Higher = clean up first."""
    weight = TYPE_WEIGHTS.get(detection["typ"], 1.0)
    return round(detection["konfidenz"] * weight, 2)

def marker_colour(score):
    """Red for high priority, orange for medium, green for low."""
    if score > 0.7:
        return "#FF4B4B"
    elif score >= 0.4:
        return "#FF8C00"
    return "#2ECC71"

# ── Reverse geocoding ─────────────────────────────────────────────────────────
@st.cache_data   # persists across sessions — each unique (lat, lon) is fetched at most once ever
def get_address(lat, lon):
    """
    Calls Nominatim for a street address.  The sleep respects the 1 req/s rate limit.
    Because of @st.cache_data, this function body only runs ONCE per unique coordinate —
    all subsequent calls (even in different sessions) return the cached result instantly.
    """
    try:
        time.sleep(1)
        url  = f"https://nominatim.openstreetmap.org/reverse?lat={lat}&lon={lon}&format=json"
        resp = requests.get(url, headers={"User-Agent": "ClearRoute-Hackathon"}, timeout=5)
        resp.raise_for_status()
        data = resp.json()

        addr   = data.get("address", {})
        road   = addr.get("road", "")
        number = addr.get("house_number", "")
        city   = (
            addr.get("city")
            or addr.get("town")
            or addr.get("village")
            or addr.get("municipality", "")
        )
        street = f"{road} {number}".strip() if number else road
        if street and city:
            return f"{street}, {city}"
        return data.get("display_name", "Adresse nicht verfügbar")
    except Exception:
        return "Adresse nicht verfügbar"

# ── Map ───────────────────────────────────────────────────────────────────────
def build_map(all_detections, marker_detections, mode="🔥 Heatmap"):
    """
    Builds the Folium map.
    mode == '🔥 Heatmap': heatmap layer + coloured markers on top.
    mode == '📍 Punkte':  coloured markers only, no heatmap.
    """
    m = folium.Map(location=[47.6603, 9.1758], zoom_start=14)

    # Only add the heatmap layer in Heatmap mode
    if mode == "🔥 Heatmap":
        heat_group = folium.FeatureGroup(name="Heatmap", show=True)
        heat_data  = [[d["lat"], d["lon"], d["konfidenz"]] for d in all_detections]
        HeatMap(heat_data, radius=20, blur=15, max_zoom=16).add_to(heat_group)
        heat_group.add_to(m)

    marker_group = folium.FeatureGroup(name="Detections", show=True)
    for d in marker_detections:
        score   = calculate_score(d)
        colour  = marker_colour(score)
        # Read from the in-session cache — instant, no API call
        address = st.session_state.addr_cache.get((d["lat"], d["lon"]), "Loading address...")

        popup_html = (
            f"<b>Address:</b> {address}<br>"
            f"<b>Type:</b> {d['typ']}<br>"
            f"<b>Score:</b> {score}<br>"
            f"<b>Confidence:</b> {d['konfidenz']:.0%}<br>"
            f"<b>Time:</b> {d['zeitstempel']}<br>"
            f"<b>Coords:</b> {d['lat']}, {d['lon']}"
        )

        folium.CircleMarker(
            location=[d["lat"], d["lon"]],
            radius=8,
            color=colour,
            fill=True,
            fill_color=colour,
            fill_opacity=0.85,
            popup=folium.Popup(popup_html, max_width=240),
            tooltip=f"{d['typ']} — score {score}",
        ).add_to(marker_group)

    marker_group.add_to(m)
    folium.LayerControl(position="topright").add_to(m)
    return m

