# ── Data loading ──────────────────────────────────────────────────────────────
REQUIRED_FIELDS = {"lat", "lon", "typ", "konfidenz", "zeitstempel"}

def load_data(source):
    """Loads and validates detection data from a file path or uploaded file."""
    try:
        if hasattr(source, "read"):
            raw = json.load(source)
        else:
            with open(source, encoding="utf-8") as f:
                raw = json.load(f)
    except json.JSONDecodeError:
        st.error("The selected file is not valid JSON. Make sure it was generated correctly by detect.py.")
        st.stop()

    if not isinstance(raw, list) or len(raw) == 0:
        st.error("The file is empty or not in the expected format (should be a list of detections).")
        st.stop()

    missing = REQUIRED_FIELDS - set(raw[0].keys())
    if missing:
        st.error(
            f"Missing fields in the data: **{', '.join(sorted(missing))}**\n\n"
            f"Expected fields: `lat`, `lon`, `typ`, `konfidenz`, `zeitstempel`."
        )
        st.stop()

    return raw

